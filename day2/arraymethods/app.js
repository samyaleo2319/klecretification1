let arr=[1,2,3,4,5];

console.log(arr);

//push()
let ans=arr.push(6,"leo",undefined);
console.log(arr);
console.log(ans);

//pop();
let ans1=arr.pop()
console.log(arr);
console.log(ans1);

//shift()
let ans3=arr.shift(6,"leo",undefined);
console.log(arr);
console.log(ans3);

// unshift()
let ans4=arr.unshift(5,"leo",undefined);
console.log(arr);
console.log(ans4);


